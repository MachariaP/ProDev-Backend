# Configuration validation on startup
def validate_mpesa_configuration():
    """Validate M-Pesa configuration on startup"""
    required_configs = ['MPESA_CONSUMER_KEY', 'MPESA_CONSUMER_SECRET', 'MPESA_PASSKEY']
    missing_configs = []
    
    for config_name in required_configs:
        if not globals().get(config_name):
            missing_configs.append(config_name)
    
    if missing_configs:
        print(f"‚ö†Ô∏è  M-Pesa Configuration Warning: Missing {', '.join(missing_configs)}")
        if MPESA_ENVIRONMENT == 'sandbox':
            print("üí° For sandbox testing, get credentials from: https://developer.safaricom.co.ke")
    else:
        print("‚úÖ M-Pesa configuration validated successfully")
        
    # Log configuration (without sensitive data)
    safe_passkey = MPESA_PASSKEY[:10] + '...' + MPESA_PASSKEY[-10:] if MPESA_PASSKEY else 'Not set'
    print(f"üì± M-Pesa Environment: {MPESA_ENVIRONMENT}")
    print(f"üîë Consumer Key: {'Set' if MPESA_CONSUMER_KEY else 'Not set'}")
    print(f"üîë Consumer Secret: {'Set' if MPESA_CONSUMER_SECRET else 'Not set'}")
    print(f"üè¶ Business Shortcode: {MPESA_BUSINESS_SHORTCODE}")
    print(f"üîê Passkey: {safe_passkey}")

# Validate configuration when settings are loaded
if not os.environ.get('DJANGO_SETTINGS_MODULE', '').endswith('test'):
    validate_mpesa_configuration()